          borderBox.addClass('isVal')
        }else{
          if(!$(this).val()){
            borderBox.removeClass('isVal')
          }
        }
      })
    },
    customSelect: function(){
      $(document).click(function(event) {
        $('.customSelect').removeClass('slideDown')
      });
      $('.customSelect').on('click',function(){
        event.stopPropagation();
        $('.customSelect').removeClass('slideDown');
        $(this).addClass('slideDown')
      }).on('click', 'li', function(event){
        event.stopPropagation();
        var par = $(this).parents('.customSelect'),
        text = $(this).text();
        par.find('.placeholder').hide();
        par.find('select').val(text).trigger('change');
        par.find('.selected-text').text(text);
        $(this).parents('.customSelect').removeClass('slideDown');
      });
      $('.customSelect select').change(function(event) {
        var val = $(this).val();
        if(val){
          var par = $(this).parent('.customSelect').parent();
          $(this).parent('.customSelect').parent().addClass('isVal').find('.error').hide();
        }
      });
    },
    btnAnimate: function(){
      $('.btn').on('mouseup',function(event) {
        $(this).addClass('btn-clicked');
        var $this =$(this);
        setTimeout(function(){
          $this.removeClass('btn-clicked')
        },500)
      });
    },
    slideUpList: function(){
      $('.faq-list li .faq-item-til').click(function(event) {
        var li = $(this).parents('li');
        if(!$(this).hasClass('open')){
          $(this).addClass('open').parents('li').siblings('li').find('.faq-item-til').removeClass('open');
          var ht = $(this).outerHeight(),
          hc = $(this).next('.faq-item-content').outerHeight();
          $('.faq-item-wrap').height(35);
          $(this).find('i').removeClass('fa-caret-right')
          li.siblings('li').find('i').addClass('fa-caret-right')
          li.find('.faq-item-wrap').height(hc+ht);
          $(this).find('i').addClass('fa-caret-down')
        }else{
          $(this).removeClass('open');
          $(this).find('i').addClass('fa-caret-right')
          li.find('.faq-item-wrap').height(35);
        }
      });
    },
    showHide: function(){
      // 显示、隐藏密码
      $('.viewPw').click(function(event) {
        var i = $(this).find('i'),
        input = $(this).parent().find('input');
        if(i.hasClass('fa-eye-slash')){
          i.removeClass().attr('class',i.data('hidden'))
          input.attr('type','text')
        }else{
          i.removeClass().attr('class',i.data('show'))
          input.attr('type','password')
        }
      });
    },
    hasEmail: function(val){
      var re = /^[A-Za-z\d]+([-_.][A-Za-z\d]+)*@([A-Za-z\d]+[-.])+[A-Za-z\d]{2,4}$/; 
      return re.test(val);
    },
    isPasswd:function (val) {  
      var re = /^(\w){6,20}$/;  
      return re.test(val)
    },
    vaildate:function($form){
      var status = true;

      $form.find('[name]').each(function(index, el) {
        var val = $.trim($(el).val()),
        $error = $(el).parent().find('.error');
        $error = el.tagName === 'SELECT' ? $(el).parent().parent().find('.error') : $error
        // 空值
        if(!val){
          status = false;
          $error.text($error.data('require'))
          return false;
        }

        // 邮箱
        if($(el).attr('name') === 'email' && !this.hasEmail(val)){
          status = false;
          $error.text($error.data('err'))
          return false;
        }

        // 密码
        if($(el).attr('name') === 'password' && !this.isPasswd(val)){
          status = false;
          $error.text($error.data('err'))
          return false;
        }

      }.bind(this));

      return status;
    },
    submit:function(){
      $('.submitBtn-1').click(function(event) {
        if(this.vaildate($('.getWhitelisted'))){
          console.log('已经通过验证')
        }
      }.bind(this));
      $('.submitBtn-2').click(function(event) {
        if(this.vaildate($('.getWhitelisted-2'))){
          console.log('已经通过验证')
        }
      }.bind(this));
      $('.submitBtn-3').click(function(event) {
        this.vaildate($('.getWhitelisted'))
      }.bind(this));
      $('.submitBtn-4').click(function(event) {
        this.vaildate($('.getWhitelisted'))
      }.bind(this));
    }
  },
  mounted(){
    this.hasBower();
    this.addBorder();
    this.getRegions();
    this.customSelect();
    this.btnAnimate();
    this.slideUpList();
    this.showHide();
    this.submit();
    new WOW({
      callback:function(box) {
        if($(box).hasClass('wow-width')){
          $(box).width($(box).data('width'));
        }
      }
    }).init();
  }
};
